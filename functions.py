# How to create functions in python

# Example 1

def add_numbers(a,b):
    return a + b

# def is short for define
# a and b are parameters

# Example 2
def sub_numbers(a,b):
    return a - b

# How to call a function

print(add_numbers(5,7))

# See the answer. the answer is 12 5+7 = 12

# Call to subtract function
print(sub_numbers(5,7))
# The answer is -2 

